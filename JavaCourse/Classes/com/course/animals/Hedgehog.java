package com.course.animals;

public class Hedgehog extends Animal
{
	// Kotzim...
	private int numOfSpikes = 10000;
	
	public Hedgehog()
	{
		super("hedi", 32);
	}
	
	public Hedgehog(String name, int age, int numOfSpikes)
	{
		super(name, age);
		this.numOfSpikes = numOfSpikes;
	}
	
	public String toString() {
		return super.toString() + " Spikes (kotzim): " + numOfSpikes;
	}
	
	@Override
	public void eat() {
		System.out.println("Hedgehog eating");
	}
	
}
